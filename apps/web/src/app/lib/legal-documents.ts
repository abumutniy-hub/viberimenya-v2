import type { LegalSection } from "../components/legal-document";
import type { PublicSiteSettings } from "./public-settings";

type LegalKind = "privacy" | "consent" | "offer" | "delivery" | "returns";

function seller(settings: PublicSiteSettings) {
  return settings.site.legalName || settings.site.brandName || "Продавец";
}

function contact(settings: PublicSiteSettings) {
  return settings.site.email || settings.phone || "контакты, указанные на сайте";
}

function customSections(value: string): LegalSection[] | null {
  const text = value.trim();
  if (!text) return null;

  const blocks = text
    .split(/\n\s*\n/)
    .map((item) => item.trim())
    .filter(Boolean);

  if (!blocks.length) return null;

  return blocks.map((block, index) => {
    const lines = block.split(/\r?\n/).map((item) => item.trim()).filter(Boolean);
    const first = lines[0] || `Раздел ${index + 1}`;
    const hasHeading = lines.length > 1 && first.length <= 120;

    return {
      title: hasHeading ? first : `Раздел ${index + 1}`,
      paragraphs: hasHeading ? lines.slice(1) : lines,
    };
  });
}

